export function getPricesByGroup(groupName, serviceType) {
  return PRICE_LIST.filter(
    item => item.group.toLowerCase().includes(groupName.toLowerCase()) ||
            (groupName === "Gent & Ladies" && item.group === "Gent & Ladies") ||
            (groupName === "Household" && item.group === "Household") ||
            (groupName === "Kids" && item.group === "Kids") ||
            (groupName === "Others" && item.group === "Others")
  ).filter(item => !serviceType || item.serviceType.toLowerCase().includes(serviceType.toLowerCase()));
}

export function getPriceByName(name, serviceType) {
  const item = PRICE_LIST.find(
    p => p.name.toLowerCase().includes(name.toLowerCase()) && 
         p.serviceType.toLowerCase() === serviceType.toLowerCase()
  );
  return item?.price || 0;
}

export function searchPrices(query) {
  const lower = query.toLowerCase();
  return PRICE_LIST.filter(
    p => p.name.toLowerCase().includes(lower) || 
         p.group.toLowerCase().includes(lower) ||
         p.serviceType.toLowerCase().includes(lower)
  );
}
